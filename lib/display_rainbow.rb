def display_rainbow (colors = ['red', 'orange', 'yellow', 'green', 'blue', 'indigo', 'violet'])

  red = ["R"]
  orange = ["O"]
  yellow = ["Y"]
  green = ["G"]
  blue = ["B"]
  indigo = ["I"]
  violet = ["V"]

  puts "R: #{colors[0]}, O: #{colors[1]}, Y: #{colors [2]}, G: #{colors[3]}, B: #{colors[4]}, I: #{colors[5]}, V: #{colors[6]}"

end
