def display_rainbow(colors)
  #red = "R: red"
  #orange =  "O: orange"
  #yellow = "Y: yellow"
  #green = "G: green"
  #blue = "B: blue"
  #indigo = "I: indigo"
  #violet = "V: violet"
  puts "R: #{colors[0]}, O: #{colors[1]}, Y: #{colors[2]}, G: #{colors[3]}, B: #{colors[4]}, I: #{colors[5]}, V: #{colors[6]}\n"
end
