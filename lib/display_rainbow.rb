def display_rainbow(array)
  red = "R"
  orange = "O"
  yellow = "Y"
  green = "G"
  blue = "B"
  indigo = "I"
  violet = "V"

  puts "#{red}: #{array[0]}, #{orange}: #{array[1]}, #{yellow}: #{array[2]}, #{green}: #{array[3]}, #{blue}: #{array[4]}, #{indigo}: #{array[5]}, #{violet}: #{array[6]}"
end

display_rainbow(['red', 'orange', 'yellow', 'green', 'blue', 'indigo', 'violet'])
