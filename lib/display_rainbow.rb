def display_rainbow(my_array)
  puts "R: #{my_array[0]}, O: #{my_array[1]}, Y: #{my_array[2]}, G: #{my_array[3]}, B: #{my_array[4]}, I: #{my_array[5]}, V: #{my_array[6]}"
end

colors = ['red', 'orange', 'yellow', 'green', 'blue', 'indigo', 'violet']
